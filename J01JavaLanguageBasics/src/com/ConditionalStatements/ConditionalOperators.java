package com.ConditionalStatements;

public class ConditionalOperators {
    public static void main(String[] args) {
        int n1 = 9, n2 = 8, n3 = 7;

        System.out.println(n1 > n2 && n1 > n3);
        System.out.println(n1 < n2 && n1 > n3);

        System.out.println(n1 > n2 || n2 < n3);
    }
}
