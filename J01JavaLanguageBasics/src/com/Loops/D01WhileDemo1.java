package com.Loops;

public class D01WhileDemo1 {
    public static void main(String[] args) {
        int i = 1;

        while(i <= 10){
            System.out.println(i);
            i++;
        }

        System.out.println("------------ 10 to 1 numbers --------------");
        i = 10;

        while (i > 0)
        {
            System.out.println(i);
            i--;
        }

    }
}
